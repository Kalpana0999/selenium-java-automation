package com.delta.kalpana.corejavaconcepts.collectionconcept.list;

public class Employee {

    int empId;
    String empName;
    String company;

    public Employee(int empId, String empName, String company) {
        this.empId = empId;
        this.empName = empName;
        this.company = company;
    }
}

class Student {

    int studRoll;
    String studName;

    public Student(int studRoll, String studName) {
        this.studRoll = studRoll;
        this.studName = studName;
    }
}